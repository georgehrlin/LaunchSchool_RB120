=begin
Which of the following are objects in Ruby? If they are objects, how can you
find out what class they belong to?

1. true
2. "hello"
3. [1, 2, 3, "happy days"]
4. 142
=end

=begin
ANSWER

1, 2, 3, 4 are all objects. Anything in Ruby with a value classifies as an
object.

To find out what class an object belongs to, I can call #class on the object.
=end

=begin
NOTES

All values in Ruby are an object.
